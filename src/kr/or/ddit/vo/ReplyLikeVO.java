package kr.or.ddit.vo;

import java.util.Date;

public class ReplyLikeVO {
	private String child_id   ;
	private String mem_id     ;
	private Date rl_date    ;
	private long mem_photo  ;
	private String mem_name   ;
	private int file_sn    ;
	
	public String getChild_id() {
		return child_id;
	}
	public void setChild_id(String child_id) {
		this.child_id = child_id;
	}
	public String getMem_id() {
		return mem_id;
	}
	public void setMem_id(String mem_id) {
		this.mem_id = mem_id;
	}
	public Date getRl_date() {
		return rl_date;
	}
	public void setRl_date(Date rl_date) {
		this.rl_date = rl_date;
	}
	public long getMem_photo() {
		return mem_photo;
	}
	public void setMem_photo(long mem_photo) {
		this.mem_photo = mem_photo;
	}
	public String getMem_name() {
		return mem_name;
	}
	public void setMem_name(String mem_name) {
		this.mem_name = mem_name;
	}
	public int getFile_sn() {
		return file_sn;
	}
	public void setFile_sn(int file_sn) {
		this.file_sn = file_sn;
	}
	
}
